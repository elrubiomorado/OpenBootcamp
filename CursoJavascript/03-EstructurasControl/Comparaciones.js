//Comparaciones en javascrip


//Igualdad ==
if(5==5){
    console.log("Igualdad");
}

//Igualdad estricta, hasta el tipo
if("5"==5){
    console.log("Igualdad estricta");
}
if("5"===5){//Checa que sean del mismo tipo o string o number, como no son del mismo tipo no entra
    console.log("Igualdad estricta");
}

//desigualdad
if(5!=4){
    console.log("5 es diferente a 4");
}

//Desigualdad estricta
if("3"!==4){
    console.log("5 es diferente a 4");
}

//Mayor que y menor que
if(5>4){
    console.log("5 es mayor 4");
}

if(2<4){
    console.log("2 es menor a 4");
}

//Mayor o igual
if(5>=5){
    console.log("5 es mayor o igual a 5");
}

//Menor o igual
if(4<=4){
    console.log("4 es menor o igual a 4");
}